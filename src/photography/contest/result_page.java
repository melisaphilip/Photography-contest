    public result_page(int cid) {
        initComponents();
        
        
        File file = new File("C:\\Users\\Lenovo\\OneDrive\\Documents\\NetBeansProjects\\Photography contest\\src\\images\\winner.jpg");
       BufferedImage bim = null;//new BufferedImage(jLabel2.getWidth(),jLabel2.getHeight(),BufferedImage.TYPE_INT_ARGB);
       try{
       bim = ImageIO.read(file);
       }
       catch(IOException e){}
       
       ImageIcon ic = new ImageIcon(bim);
       Image img = ic.getImage();
       Image myimg = img.getScaledInstance(600,450,Image.SCALE_SMOOTH);
       ImageIcon finimg = new ImageIcon(myimg);
       jLabel1.setIcon(finimg);
        
        
        try{
            this.cid=cid;
            this.pid=pid;
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/public_photography","root","Melisa@28");
            Statement st = con.createStatement();
            String sql = "SELECT name, email, caption,image FROM participants where contest_id="+cid+" AND vote_count=(SELECT MAX(vote_count) FROM participants)";
            ResultSet res = st.executeQuery(sql);
            while(res.next()){
                        byte[] img1 = res.getBytes("image");
                        ImageIcon image = new ImageIcon(img1);
                        Image im = image.getImage();
                        //Image myimg = im.getScaledInstance(jLabel4.getWidth(), jLabel4.getHeight(), Image.SCALE_SMOOTH);
                        Image myimg1 = im.getScaledInstance(343, 221, Image.SCALE_SMOOTH);
                        ImageIcon newImage = new ImageIcon(myimg1);
                        jLabel5.setIcon(newImage);
                
                        jLabel3.setText(res.getString("name"));
                        jLabel4.setText(res.getString("email"));
                        jLabel2.setText(res.getString("caption"));
            }
        }
        catch(SQLException e){
             JOptionPane.showMessageDialog(null, e);
         }
         catch(Exception e){
             JOptionPane.showMessageDialog(null, e);
         }
    }

